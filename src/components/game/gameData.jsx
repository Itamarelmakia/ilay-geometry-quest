export function getLevelById(levelId) {
  for (const world of WORLDS) {
    for (const level of world.levels) {
      if (level.id === levelId) return { ...level, worldId: world.id, worldName: world.name, worldColor: world.color };
    }
  }
  return null;
}

export function getWorldById(worldId) {
  return WORLDS.find(w => w.id === worldId);
}

export function calculatePlayerLevel(xp) {
  if (xp < 50) return 1;
  if (xp < 150) return 2;
  if (xp < 300) return 3;
  if (xp < 500) return 4;
  if (xp < 800) return 5;
  if (xp < 1200) return 6;
  return 7;
}

export function xpForNextLevel(xp) {
  const thresholds = [50, 150, 300, 500, 800, 1200, 2000];
  for (const t of thresholds) {
    if (xp < t) return t;
  }
  return 2000;
}